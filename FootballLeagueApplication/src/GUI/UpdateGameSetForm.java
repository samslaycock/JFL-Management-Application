    private void cmbTeamPopulate(){
        Team t = new Team("jflDB");
        String[] teams = t.populateTeamComboBox();
        
       for( int i=0; i<teams.length ; i++){
           cmbHomeTeam1.addItem(teams[i]);
           cmbHomeTeam2.addItem(teams[i]);
           cmbAwayTeam1.addItem(teams[i]);
           cmbAwayTeam2.addItem(teams[i]);
        }
 
        t.closeConnection(); 
    }
    
    private void loadReferees(){
        Referee r = new Referee("jflDB");
        Object[][] refereeRecords = r.loadAllReferees();
        String firstName;
        String lastName;
        
       for( int i=0; i<refereeRecords.length ; i++){
           firstName = refereeRecords[i][2].toString();
           lastName = refereeRecords[i][3].toString();
           cmbReferee1.addItem(firstName+" "+lastName);
           cmbReferee2.addItem(firstName+" "+lastName);
        }
 
        r.closeConnection(); 
        
    }
    
    private void cmbTeamSelect(String teamBox){
        int selectedIndex;
        int otherIndex;
        
        if (teamBox == "HOME"){
            selectedIndex = cmbHomeTeam1.getSelectedIndex();
            otherIndex = cmbAwayTeam1.getSelectedIndex();
            
            if (selectedIndex == otherIndex){
                cmbAwayTeam1.setSelectedIndex(0);
            }
            
            cmbAwayTeam2.setSelectedIndex(selectedIndex);
        }else if(teamBox == "AWAY"){
            selectedIndex = cmbAwayTeam1.getSelectedIndex();
            otherIndex = cmbHomeTeam1.getSelectedIndex();
            
            if (selectedIndex == otherIndex){
                cmbHomeTeam1.setSelectedIndex(0);
            }
            
            cmbHomeTeam2.setSelectedIndex(selectedIndex);
            
        }
          
    }
    
    private void loadGameSet(int gameSet){
        Game g = new Game("jflDB");
        Object[][] gameSetRecord = g.loadDetails(gameSet);
        g.closeConnection();
        
        Team t = new Team("jflDB");
        String homeTeam = t.getTeamName(Integer.parseInt(gameSetRecord[0][2].toString()));
        String awayTeam = t.getTeamName(Integer.parseInt(gameSetRecord[1][2].toString()));
        t.closeConnection();
        
        cmbHomeTeam1.setSelectedItem(homeTeam);
        cmbAwayTeam1.setSelectedItem(awayTeam);
        
        spnHomeGoals1.setValue(Integer.parseInt(gameSetRecord[0][4].toString()));
        spnAwayGoals1.setValue(Integer.parseInt(gameSetRecord[0][5].toString()));
        
        spnHomeGoals2.setValue(Integer.parseInt(gameSetRecord[1][4].toString()));
        spnAwayGoals2.setValue(Integer.parseInt(gameSetRecord[1][5].toString()));
        
        txtDate1.setText(gameSetRecord[0][7].toString());
        txtDate2.setText(gameSetRecord[1][7].toString());
        
        Referee r = new Referee("jflDB");
        Object[] refereeRecord = r.loadDetails(Integer.parseInt(gameSetRecord[0][6].toString()));
        String firstName = refereeRecord[1].toString();
        String lastName = refereeRecord[2].toString();
        String fullName = firstName+" "+lastName;
        
        cmbReferee1.setSelectedItem(fullName);
        
        refereeRecord = r.loadDetails(Integer.parseInt(gameSetRecord[1][6].toString()));
        firstName = refereeRecord[1].toString();
        lastName = refereeRecord[2].toString();
        fullName = firstName+" "+lastName;
        
        cmbReferee2.setSelectedItem(fullName);
        
        }

